package com.literals;

public class CharacterLiterals {

	public static void main(String[] args) {
		
		// char maximum range 0 to 65535
		
		char c ='K';    // single character in single quote
		char c1 = 66;   // ASCII code 
		char c2 = '\u0040';  // Uni-code
		
		//char c3 ='Ke'; Invalid
		
		System.out.println(c);
		System.out.println(c1);
		System.out.println(c2);
		

	}

}
