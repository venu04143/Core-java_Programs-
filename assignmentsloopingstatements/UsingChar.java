package com.assignmentsloopingstatements;

public class UsingChar {
	public static void main(String[] args) {
		
		for(char a='A';a<='Z';a++) {
			System.out.print(a+ " ");
		}
		
		System.out.println();
		
		for (char c=65;c<99;c++) {
			System.out.print(c+" ");
		}
		
		System.out.println();
		
		for(char c= 0;c<10;c++) {
			System.out.print(c+ " ");
		}
		System.out.println();
		for(char a='a';a<='z';a++) {
			System.out.print(a + " ");
		}
		
		
		
	}

}
